package class2;

public class VariableValues {
    public static void main(String[] args) {
        boolean happy=true;
        happy=false;
        System.out.println(happy);

        float patient=36.6f;
        patient=39.8f;
        System.out.println(patient);

        byte apple=4;
        apple=5;
        System.out.println(apple*2);
        System.out.println(apple*10);
        System.out.println(apple*40);
    }


}
