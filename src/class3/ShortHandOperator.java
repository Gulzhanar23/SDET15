package class3;

public class ShortHandOperator {
    public static void main(String[] args) {
        int number=10;
        number+=10;
        System.out.println(number);

        int number2=20;
        number2*=20;
        System.out.println(number2);

        int number3=100;
        number3/=10;
        System.out.println(number3);

        int number4=20;
        number4-=10;
        System.out.println(number4);
    }
}
