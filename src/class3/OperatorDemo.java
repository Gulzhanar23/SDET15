package class3;

public class OperatorDemo {
    public static void main(String[] args) {
        int number=10;
        System.out.println(number/3);
        System.out.println(number%3);
        float number1=10;
        System.out.println(number1/3);
        System.out.println(number1%3);
        System.out.println(16/5);
        System.out.println(16%5);
    }
}
